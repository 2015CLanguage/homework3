#include<stdio.h>
int main()
{
	int x,y,a,b,c,d;
	printf("请输入坐标：x，y\n");
	scanf("%d,%d",&x,&y);
	a=(x+2)*(x+2)+(y-2)*(y-2);  //高度为10点的特征
	b=(x+2)*(x+2)+(y+2)*(y+2);  //高度为10点的特征
	c=(x-2)*(x-2)+(y-2)*(y-2);  //高度为10点的特征
	d=(x-2)*(x-2)+(y+2)*(y+2);  //高度为10点的特征
	if(a<=1||b<=1||c<=1||d<=1)  //判断，点满足特征 
		printf("高度为10\n");
	else    //判断，点不满足特征 
		printf("高度为0\n");
	return 0;
}
