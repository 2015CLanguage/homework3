#include<stdio.h>
int main()
{
	float x,y,t1,t2,t3,t4;
	printf("请输入横坐标x:");   //提示用户输入横坐标
	scanf("%f",&x);
	printf("请输入纵坐标y:");   //提示用户输入纵坐标
	scanf("%f",&y);
	t1=(x-2)*(x-2)+(y-2)*(y-2); //分别计算输入的点到四个圆心的距离的平方
	t2=(x+2)*(x+2)+(y-2)*(y-2);
	t3=(x-2)*(x-2)+(y+2)*(y+2);
	t4=(x+2)*(x+2)+(y+2)*(y+2);
	if(t1>1&&t2>1&&t3>1&&t4>1)  //若四个距离的平方都大于1，则建筑物高度为0
		printf("建筑物高度为%d\n",0);
	else                        //否则，建筑物高度为10米
		printf("建筑物高度为%d米\n",10);
	return 0;
}
